console.log(typeof despedidaGolfinho);


// Number
let  minhaIdade = 17;
console.log(minhaIdade);
console.log(typeof 17);


// Boolean
let estouVivo = true;
console.log(estouVivo);
console.log(typeof estouVivo);


// Array
let meuNumeroArray = [10,15,40];
console.log(meuNumeroArray);
console.log(meuNumeroArray[2]); // deve retornar 40
console.log(typeof meuNumeroArray);


// Object
let cachorro = { nome : 'Totó', raca : 'Dálmata' };
console.log(cachorro);
console.log(cachorro.nome) // deve retornar Totó
console.log(typeof cachorro);



// Null
let x = null;
console.log(x);
console.log(typeof x);

