// Initialize array of shark species with array literal
let sharks = [
    "Hammerhead",
    "Great White",
    "Tiger",
];

console.log(sharks); // [ 'Hammerhead', 'Great White', 'Tiger' ]

// Initialize array of shark species with array constructor
let sharks2 = new Array(
	"Hammerhead",
	"Great White",
	"Tiger",
);

console.log(sharks); // [ 'Hammerhead', 'Great White', 'Tiger' ]
