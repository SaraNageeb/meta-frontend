// String
let city = 'London';
console.log(`The typeof: ${city} is ${typeof city}`);

// Number
let  population = 8.982;
console.log(`The typeof: ${population} is ${typeof population}`);

// Boolean
let hasGoodWeather = false;
console.log(`The typeof: ${hasGoodWeather} is ${typeof hasGoodWeather}`);


// Array
let footballTeams = ["Arsenal", "Chelsea", "Spurs", "West Ham"];
console.log(`The typeof: ${footballTeams} is ${typeof footballTeams}`);


// Object
let chelsea = {captain : 'Frank Lampard', manager : 'Jose Mourinho'};
console.log(`The typeof: ${chelsea} is ${typeof chelsea}`);


// Null
let x = null;
console.log(`The typeof: ${x} is ${typeof x}`);

// NaN
let fruit = 'apple';
let luckyNumber = 4;
let notAnumber = fruit * luckyNumber;
console.log(`The typeof: ${notAnumber} is ${typeof notAnumber}`);
